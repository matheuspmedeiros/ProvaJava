package br.etec.prova.model;

public class Horista {
	private Integer id_horista;
	private String PrecoHora;
	private String HorasTrabalhadas;
	
	
	public Integer getId_horista() {
		return id_horista;
	}
	public void setId_horista(Integer id_horista) {
		this.id_horista = id_horista;
	}
	public String getPrecoHora() {
		return PrecoHora;
	}
	public void setPrecoHora(String precoHora) {
		PrecoHora = precoHora;
	}
	public String getHorasTrabalhadas() {
		return HorasTrabalhadas;
	}
	public void setHorasTrabalhadas(String horasTrabalhadas) {
		HorasTrabalhadas = horasTrabalhadas;
	}
	
	
}
