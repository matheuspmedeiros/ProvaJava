package br.etec.prova.model;

public class Comissionado {
	private Integer id_comissionado;
	private String Total_vendas;
	private String Taxa_Comissionado;
	
	
	public Integer getId_comissionado() {
		return id_comissionado;
	}
	public void setId_comissionado(Integer id_comissionado) {
		this.id_comissionado = id_comissionado;
	}
	public String getTotal_vendas() {
		return Total_vendas;
	}
	public void setTotal_vendas(String total_vendas) {
		this.Total_vendas = total_vendas;
	}
	public String getTaxa_Comissionado() {
		return Taxa_Comissionado;
	}
	public void setTaxa_Comissionado(String taxa_Comissionado) {
		this.Taxa_Comissionado = taxa_Comissionado;
	}
	

}
